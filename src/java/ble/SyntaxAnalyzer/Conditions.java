public class Conditions {
    public static String getConditionPattern() {
        return "if(\\s*\\(\\s*(\\-?\\d+|\\-?\\d+\\.\\d+|[A-z][\\w\\_]*)\\s*)*"+
			"(\\s*(<|>|==|<=|>=|!=|!|&&|\\|\\||\\^)\\s*(\\-?\\d+|\\-?\\d+\\.\\d+|[A-z][\\w\\_]*)\\s*)*\\)\\s*"+
			"\\r\\n(\\t.+(\\r\\n)*)*"+
			"(else if\\s*\\(\\s*(\\-?\\d+|\\-?\\d+\\.\\d+|[A-z][\\w\\_]*)\\s*"+
			"(\\s*(<|>|==|<=|>=|!=|!|&&|\\|\\||\\^)\\s*(\\-?\\d+|\\-?\\d+\\.\\d+|[A-z][\\w\\_]*))*\\s*\\)\\s*"
			+ "\\r\\n(\\t.+(\\r\\n)*)+)*"+
			"(else\\s*\\(\\s*(\\-?\\d+|\\-?\\d+\\.\\d+|[A-z][\\w\\_]*)\\s*"+
			"(\\s*(<|>|==|<=|>=|!=|!|&&|\\|\\||\\^)\\s*(\\-?\\d+|\\-?\\d+\\.\\d+|[A-z][\\w\\_]*))*\\s*\\)\\s*"
			+ "\\r\\n(\\t.+(\\r\\n)*)+)?";
	
    }
}
